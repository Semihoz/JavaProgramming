package day17_While_DoWhile;

public class LoopPractices {

    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++) { //For Loop
            System.out.println(i);
        }
        System.out.println("====================================================================");
        int j = 1;            //While Loop
        while (j<= 10 ){

            System.out.println(j);
            j++;;
        }
        System.out.println("================================================================");
        int k = 1;                                        //Do While Loop
        do {
            System.out.println(k);
            k++;
        }while (k <= 10);


















    }
}
