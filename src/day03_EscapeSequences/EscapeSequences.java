package day03_EscapeSequences;
/*
Escape sequences: MUST be given with a double quote
    \n: starts a new line
*/
public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("java \nPython \nC#");

        System.out.println("----------------------------------------");

        System.out.println("\tHello CYDEO How are you all today? \nIts nice to see you all! \nWhat class do we have next week?");

        System.out.println("---------------------------------------------------------");

        System.out.println("/ \\");

        System.out.println("---------------------------------------------------------------");

        System.out.println("My favorite TV show is \"Game Of Thrones\"");



    }




}
